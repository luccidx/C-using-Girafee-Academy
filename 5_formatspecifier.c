#include <stdio.h>
#include <stdlib.h>

int main(){
    printf("My Age is %d \n",21);
    //%d is integer format specifier
    printf("My GPA is %f \n",8.74);
    //%f for double
    printf("My Grade is %c \n",'A');
    //%c for char
    printf("My Branch is %s \n","E&C");
    //%s for array of char(string)

    return 0;
}